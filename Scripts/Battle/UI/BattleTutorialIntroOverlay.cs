	public override async void _Ready()
	{
		if (_shown)
		{
			return;
		}

		_shown = true;
		await ShowPopupAsync();
	}

	private async Task ShowPopupAsync()
	{
		await ToSignal(GetTree(), SceneTree.SignalName.ProcessFrame);
		if (PopupScene?.Instantiate() is not PagedTutorialPopup popup)
		{
			return;
		}

		(string title, string[] pages) = ResolvePopupContent();
		(GetTree().CurrentScene ?? this).AddChild(popup);
		popup.Present(title, pages, onCompleted: null, onClosed: null);
	}

	private (string Title, string[] Pages) ResolvePopupContent()
	{
		if (!string.IsNullOrWhiteSpace(TutorialPresetId)
			&& TutorialPresets.TryGetValue(TutorialPresetId, out (string Title, string[] Pages) preset))
		{
			return preset;
		}

		return (
			string.IsNullOrWhiteSpace(PopupTitle) ? "\u6559\u7A0B" : PopupTitle,
			PopupPages ?? Array.Empty<string>());
	}
